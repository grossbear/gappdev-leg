#include "memory_allocator.h"
#include "memory_allocator_new.h"

/////////////////////////////////////////////////////////////////////////////////////////////
void* memory_new_allocator::alloc(uisize_t size)
{
    return ::operator new(size);
}

/////////////////////////////////////////////////////////////////////////////////////////////
void memory_new_allocator::dealloc(void *ptr)
{
    return ::operator delete(ptr);
}

